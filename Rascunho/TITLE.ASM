PROC MAIN
MOV AX,@DATA
MOV DS,AX
MOV ES,AX

MOV AH,0
MOV AL,13h ; Modo VGA 320X200 coloração 16b
INT 10h

MOV AX,9               ; Forma do cursor
MOV BX,0
MOV CX,0
MOV BP,SEG CURSOR          ;ES:DX Ponteiro de 16 bits para o cursor
MOV DX,OFFSET CURSOR
INT 33h

MOV AX,1 ; usar cursor
INT 33h

MOV AH,1
INT 21h

XOR AL,AL

MOV CX,0
MOV DX,0

INICIO:

IN AL,60h
CMP AL,10h
JE QUITTER

MOV AX,3 ; Pegar coordenadas do cursor
MOV BH,0
INT 10h

MOV AH,2
INT 21h
MOV AH,2
MOV DL,DH
INT 21h

JMP INICIO

QUITTER:

MOV AH,4Ch
INT 21h
ENDP MAIN
END MAIN